public class Pokemon {
    public Pokemon(int pokedex_number, String name, int speed, String special_defence, String special_attack, int defence, int attack, int hp, String primary_type, String secondary_type) {
        this.pokedex_number = pokedex_number;
        this.name = name;
        this.speed = speed;
        this.special_defence = special_defence;
        this.special_attack = special_attack;
        this.defence = defence;
        this.attack = attack;
        this.hp = hp;
        this.primary_type = primary_type;
        this.secondary_type = secondary_type;
    }

    private int pokedex_number;
    private String name;
    private int speed;
    private String special_defence;
    private String special_attack;
    private int defence;
    private int attack;
    private int hp;
    private String primary_type;
    private String secondary_type;


}
